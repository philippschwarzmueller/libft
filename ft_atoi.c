int	ft_atoi(const char *str)
{
	int	res;
	int	i;

	res = 0;
	i = 0;

	if (str[i] >= 48 && str[i] >= 57)
	{
		res *= 10;
		res += str[i] - '0';
		i++;
	}
	return (res);
}
