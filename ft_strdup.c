#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*res;
	int		i;

	res = malloc((ft_strlen(s1) + 1) * sizeof(*s1));
	i = 0;
	if (!res)
	{
		return (NULL);
	}
	while (s1[i] != '\0')
	{
		res[i] = s1[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
